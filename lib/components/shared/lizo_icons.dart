  static Widget _svgIcon(String assetName, double size, Color? color) {
    return SvgPicture.asset(
      assetName,
      height: size,
      width: size,
      colorFilter:
          color != null ? ColorFilter.mode(color, BlendMode.srcIn) : null,
      fit: BoxFit.contain,
    );
  }
